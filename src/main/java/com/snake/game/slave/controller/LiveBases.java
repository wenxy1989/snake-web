package com.snake.game.slave.controller;

public class LiveBases {
	
	private Long longth;
	private Long width;
	private Long height;
	private Long weight;
	private Long energy;
	private Long spirit;
	private Long hardness;

	public void setLongth(Long longth) {
		this.longth = longth;
	}

	public void setWidth(Long width) {
		this.width = width;
	}

	public void setHeight(Long height) {
		this.height = height;
	}

	public void setWeight(Long weight) {
		this.weight = weight;
	}

	public void setEnergy(Long energy) {
		this.energy = energy;
	}

	public void setSpirit(Long spirit) {
		this.spirit = spirit;
	}

	public void setHardness(Long hardness) {
		this.hardness = hardness;
	}

	public Long getLongth() {
		return longth;
	}

	public Long getWidth() {
		return width;
	}

	public Long getHeight() {
		return height;
	}

	public Long getWeight() {
		return weight;
	}

	public Long getEnergy() {
		return energy;
	}

	public Long getSpirit() {
		return spirit;
	}

	public Long getHardness() {
		return hardness;
	}

}
